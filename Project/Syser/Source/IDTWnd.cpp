bool CIDTWnd::InsertIDT()
{	
	WISP_CHAR display[64];
	HANDLE hItem;

	DWORD dwSelector,dwIDTSize,dwOffset;
	VADDR32 dwIDTBase;
//	DWORD dwLow,dwHigh;
//	DWORD dwSegmentBase,dwSegmentLimit;
	DWORD i = 0,j;		
	const WCHAR *Format=WSTR("%04x  %s  %04x:%08x  DPL=%d %s");
//	WCHAR DisplayBuf[0x200];
	X86_GATE_DECR *pGateDecr;
	static const WCHAR *DescType[16]={
		WSTR("Reserved"),
		WSTR("TSS16   "),
		WSTR("LDT     "),
		WSTR("TSS32   "),
		WSTR("CallG16 "),
		WSTR("TaskG   "),
		WSTR("IntG16  "),
		WSTR("TrapG16 "),
		WSTR("Reserved"),
		WSTR("TSS32   "),
		WSTR("Reserved"),
		WSTR("TSS32   "),
		WSTR("CallG32 "),
		WSTR("Reserved"),
		WSTR("IntG32  "),
		WSTR("TrapG32 "),
	};

	dwIDTBase = PTR_TO_NUM(m_pIDTAddress);
	dwIDTSize = m_dwIDTSize;

	j = (dwIDTSize + 1) / sizeof(X86_GATE_DECR);
	pGateDecr = (X86_GATE_DECR*)NUM_TO_PTR(dwIDTBase);

	
	for(i = 0;i < j;i++)
	{
		dwSelector = pGateDecr[i].Selector;
		TSPrintf(display,WISP_STR("%02X"),i);
		hItem = InsertItem(display);
		SetItemText(hItem,1,DescType[pGateDecr[i].Type]);
		dwOffset = pGateDecr[i].OffsetLow | (pGateDecr[i].OffsetHigh << 16);
#ifdef CODE_OS_NT_DRV
		extern DWORD			gOldInterruptProcAddress[0x100];
		if(gOldInterruptProcAddress[i])
		{
			DWORD SelfStart = *(DWORD*)&gpSyser->m_DriverObject->DriverStart;		
			if(dwOffset>SelfStart&&dwOffset<SelfStart+gpSyser->m_DriverObject->DriverSize)
				dwOffset=gOldInterruptProcAddress[i];
		}
#endif
		SetItemText(hItem,2,DescType[pGateDecr[i].Type]);		
		TSPrintf(display,WISP_STR("%04X:%08X"),dwSelector,dwOffset);
		SetItemText(hItem,2,display);
		TSPrintf(display,WISP_STR("DPL=%d %s"),pGateDecr[i].DPL,pGateDecr[i].P ? WSTR("P "):WSTR("NP"));
		SetItemText(hItem,3,display);
	}
	return true;
}

